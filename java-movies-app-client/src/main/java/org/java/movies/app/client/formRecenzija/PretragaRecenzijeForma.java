    private void btnPretraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziActionPerformed

        try {
            
            Film film=(Film) cbdFilmovi.getSelectedItem();
            
            if(film==null)
                throw  new Exception("Selektujte film!");
            Recenzija recenzija=new Recenzija(Long.MIN_VALUE, null, Integer.MIN_VALUE, null, null, film);
            List<Recenzija> recenzije = Communication.getInstance().findRecenzije(recenzija);
            if (recenzije.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Nema recenzija za zadati kriterijum pretrage!");
            } else {
                tableRecenzije.setModel(new RecenzijaTableModel(recenzije));
                JOptionPane.showMessageDialog(this, "Pronadjene su recenzije!", "Poruka", JOptionPane.INFORMATION_MESSAGE);

            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnPretraziActionPerformed

    private void btnIzaberiRecenzijuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzaberiRecenzijuActionPerformed
       try{
                PregledajObrisiRecenzijuForma pf = (PregledajObrisiRecenzijuForma) this.getOwner();
                int selectedRow=tableRecenzije.getSelectedRow();
                if(selectedRow<0)
                    throw new Exception("Recenzija mora biti selektovana!");
                RecenzijaTableModel model=(RecenzijaTableModel) tableRecenzije.getModel();
                Recenzija recenzija = model.getRecenzija(selectedRow);
                if (recenzija == null) {
                    throw new Exception("Recenzija mora biti selektovana!");
                }
                pf.setRecenzija(recenzija);
                this.dispose();
       }catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska", JOptionPane.ERROR_MESSAGE);
            }

    }//GEN-LAST:event_btnIzaberiRecenzijuActionPerformed

