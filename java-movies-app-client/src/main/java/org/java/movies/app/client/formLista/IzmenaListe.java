    private void prepareView() {
        tblFilmovi.setModel(new SimpleFilmTableModel(lista.getFilmovi()));
        try {
            List<Film> filmovi = Communication.getInstance().getFilmovi();
            cbdFilmovi.removeAllItems();
            for (Film film : filmovi) {
                cbdFilmovi.addItem(film);
            }
            cbdFilmovi.setSelectedItem(null);
            txtNaziv.setText(lista.getNazivListe());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }
