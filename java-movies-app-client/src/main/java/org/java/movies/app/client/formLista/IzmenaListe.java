    private void btnObrisiFilmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiFilmActionPerformed
        try {
            SimpleFilmTableModel model = (SimpleFilmTableModel) tblFilmovi.getModel();

            int selectedRow = tblFilmovi.getSelectedRow();
            if (selectedRow < 0) {
                throw new Exception("Selektujte film!");
            }
            Film film = model.getFilm(selectedRow);
            model.delete(film);
            pomocna.get(selectedRow).setStatus(Status.OBRISAN);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnObrisiFilmActionPerformed

    private void btnDodajFilmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajFilmActionPerformed
        try {
            Film film = (Film) cbdFilmovi.getSelectedItem();
            film.setStatus(Status.DODAT);
           
            if (film == null) {
                throw new Exception("Unesite pravilo sve podatke!");
            }

            SimpleFilmTableModel model = (SimpleFilmTableModel) tblFilmovi.getModel();
            List<Film> filmovi = model.getFilmovi();
            if (film!=null && filmovi.contains(film)) {
                throw new Exception("Film je vec dodat!");
            }else{
                pomocna.add(film);
                model.add(film);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_btnDodajFilmActionPerformed

